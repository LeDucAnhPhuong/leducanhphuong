export const calculateSizes = (
  isSmall: boolean,
  isMobile: boolean,
  isTablet: boolean
) => {
  return {
    deskScale: isSmall ? 0.045 : isMobile ? 0.05 : 0.065,
    deskPosition: isMobile ? [0.5, -4.5, -1] : [0.25, -5.5, -1],
    cubePosition: isSmall
      ? [4, -4, 0]
      : isMobile
        ? [4, -4, 2]
        : isTablet
          ? [5, -4, 2]
          : [9, -4.5, 2],
    reactLogoPosition: isSmall
      ? [3, 0, 0]
      : isMobile
        ? [4, 4, 0]
        : isTablet
          ? [8, 4, 0]
          : [12, 3, 0],
    ringPosition: isSmall
      ? [-3, 3, 1]
      : isMobile
        ? [-4, 5, 1]
        : isTablet
          ? [-5, 5, 2]
          : [-10, 4, 2],
    targetPosition: isSmall
      ? [-5, -9, -10]
      : isMobile
        ? [-8, -9, -10]
        : isTablet
          ? [-11, -7, -10]
          : [-13, -13, -10],
  };
};

